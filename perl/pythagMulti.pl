# iterate through A,B,C to generate W,X,Y,Z such that W²+X²+Y²=Z²
my $N = 10;
for my $A (1 .. $N) {
    for my $B ( 1 .. $N ) {
        for my $C ( 1 .. $N ) {
            my $W = abs($A**2 - $B**2 - $C**2);
            my $X = 2*$A*$B;
            my $Y = 2*$A*$C;
            my $Z = $A**2 + $B**2 + $C**2;
            my @order = sort $W,$X,$Y;
            my $order_string = join ',', @order;
            if( 1 == ++$track{$Z}{order}{$order_string} ) {
                #    print "$order_string => $Z\n";
                $track{$Z}{subtotal}{$W}++;
                $track{$Z}{subtotal}{$X}++ unless $X == $W;
                $track{$Z}{subtotal}{$Y}++ unless $Y == $W or $Y == $X;
            }
        }
    }
}

sub ordersort
{
    scalar(keys %{$track{$b}{order}}) <=> scalar(keys %{$track{$a}{order}})
    ||
    $b <=> $a
}

